#include "vectorobj.h"

void	*vector_add(t_obj *this, va_list ap)
{
	void	*dst;
	long	n;

	n = this->meta->size;
	vector_resize(this, n + 1);
	dst = vector_getitem(this, n);
	this->put(dst, ap, this->meta);
	return (dst);
}
