#include "libft.h"
#include "vectorobj.h"

void	sort(t_obj *vect, t_cmp cmp)
{
	if (vect->meta->size > 1)
		ft_qsort(vect->meta->data, vect->meta->size, vect->meta->itemsize, cmp);
}
