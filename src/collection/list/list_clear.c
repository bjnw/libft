#include <stdlib.h>

#include "listobj.h"

void	list_clear(t_obj *list)
{
	if (list->erase)
		foreach(list, list->erase);
	free(list->data);
	list->data = NULL;
	list->size = 0;
	((t_list *)list)->capacity = 0;
}
