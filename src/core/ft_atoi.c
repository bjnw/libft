#include <stdbool.h>

#include "libft.h"

int	ft_atoi(const char *s)
{
	int		n;
	bool	neg;

	n = 0;
	while (ft_isspace(*s))
		s++;
	neg = (*s == '+') ^ (*s == '-') && *s++ == '-';
	while (ft_isdigit(*s))
		n = (n << 3) + (n << 1) + (*s++ & ~060);
	return ((int []){n, -n}[neg]);
}
