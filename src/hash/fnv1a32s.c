#include "fnv.h"

uint32_t	fnv1a32s(const char *s)
{
	uint32_t ret;

	ret = FNV1A32_OFFSET_BASIS;
	while (*s)
	{
		ret ^= (uint8_t)*s++;
		ret *= FNV1A32_PRIME;
	}
    return (ret);
}
